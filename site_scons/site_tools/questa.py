def compile_simlib(target, source, env):

    dirs = env['DIRS']
    ext  = env['EXT']
    
    trg = target[0]

    trg_path = str(trg)
    trg_dir  = str(trg.dir)

    print('compile lib  \'' + trg.name + '\'')
    
    if not os.path.exists(trg_path):
        #print('create lib  \'' + trg.name + '\'')
        rcode = pexec(env['VLIBCOM'] + ' ' + trg.name, trg_dir)
        if rcode: return rcode
        
        rcode = pexec(env['VMAPCOM'] + ' -c', trg_dir)
        if rcode: return rcode

        cmd = []
        cmd.append(env['VMAPCOM'])
        cmd.append(trg.name)
        cmd.append(os.path.abspath(trg_path))
        cmd = ' '.join(cmd)

        if env['VERBOSE']:
          print(cmd)
        
        rcode = pexec(cmd, trg_dir)
        if rcode: return rcode
                            
    for src in source:
        cmd = []
        cmd.append(env['VSIMCOM'])
        cmd.append(' -batch')
        cmd.append(' -do ' + os.path.abspath(str(src)))
        cmd = ' '.join(cmd)
        
        ip_name = src.name.replace('-ipsim.'+ext.SIM_SCRIPT, '')
        print('-'*80)
        print(' '*8, 'Compile simlib for', '\'' + ip_name + '\'')
        rcode = pexec(cmd, dirs.IP_OOC)
        print('-'*80)
        if rcode: 
            Execute( Delete(src) )        
            return rcode
        
    return None
